/**
 * S-12: Suggest the closest matching anchor when a broken-anchor warning
 * fires. Uses a cheap two-pass match:
 *   1. Exact substring match (anchor is contained in or contains a heading)
 *   2. Levenshtein-like edit-distance within a budget (max 3 edits)
 *
 * Returns the best-matching slug string, or null when no candidate scores
 * well enough to suggest with confidence. Suggestion threshold tuned so
 * cosmetic typos surface but unrelated headings don't false-positive.
 *
 * @param {string} broken - the slug the user wrote (e.g. "athena-setup")
 * @param {Set<string>} candidates - anchors that exist in the target doc
 * @returns {string|null}
 */
export function suggestAnchor(broken, candidates) {
  if (!broken || !candidates || candidates.size === 0) return null;

  // Pass 1: substring containment — high-confidence match. Both sides must
  // be at least 4 chars to avoid spurious matches against very short anchors
  // (e.g. `#a` would otherwise match any broken slug containing the letter a).
  const MIN_SUBSTRING = 4;
  for (const c of candidates) {
    if (c.length < MIN_SUBSTRING || broken.length < MIN_SUBSTRING) continue;
    if (c.startsWith(broken) || broken.startsWith(c) || c.includes(broken) || broken.includes(c)) {
      // Additionally require >= 50% overlap of the shorter into the longer.
      // Avoids "user-id" matching "user-management-and-administration" via
      // the bare "user" prefix.
      const overlap = Math.min(c.length, broken.length) / Math.max(c.length, broken.length);
      if (overlap >= 0.5) return c;
    }
  }

  // Pass 2: edit distance — pick the closest if within budget.
  let best = null;
  let bestDist = Infinity;
  for (const c of candidates) {
    // Cheap early-out: huge length difference can't be within budget.
    if (Math.abs(c.length - broken.length) > 8) continue;
    const d = editDistance(broken, c);
    if (d < bestDist) { bestDist = d; best = c; }
  }
  // Budget: max(3, length / 5) — proportional to slug length but cap small.
  const budget = Math.max(3, Math.floor(broken.length / 5));
  if (bestDist <= budget) return best;
  return null;
}

/**
 * Levenshtein edit distance. O(m·n) time, O(min) space. We bound input
 * size before calling (S-12's pass 2 pre-filters), so a textbook impl is
 * fine. Adding a dependency for one cheap routine isn't worth it.
 */
function editDistance(a, b) {
  if (a === b) return 0;
  if (!a.length) return b.length;
  if (!b.length) return a.length;
  let prev = new Array(b.length + 1);
  let curr = new Array(b.length + 1);
  for (let j = 0; j <= b.length; j++) prev[j] = j;
  for (let i = 1; i <= a.length; i++) {
    curr[0] = i;
    for (let j = 1; j <= b.length; j++) {
      const cost = a[i - 1] === b[j - 1] ? 0 : 1;
      curr[j] = Math.min(
        prev[j] + 1,        // deletion
        curr[j - 1] + 1,    // insertion
        prev[j - 1] + cost, // substitution
      );
    }
    [prev, curr] = [curr, prev];
  }
  return prev[b.length];
}

function resolveTarget(sourcePath, targetRel, projectDir) {
  if (!targetRel) return null;
  // B-6: try BOTH the literal path and the URL-decoded form. CommonMark
  // accepts `[name](../WU%20Documentation/foo.md)` for paths with spaces,
  // and the decoded form (`../WU Documentation/foo.md`) is what hits the
  // filesystem. The angle-bracket form `<../WU Documentation/foo.md>` is
  // already non-URL-encoded by the time it reaches us. Try literal first
  // (handles paths that legitimately contain `%`), then decoded.
  const candidates = [targetRel];
  try {
    const decoded = decodeURIComponent(targetRel);
    if (decoded !== targetRel) candidates.push(decoded);
  } catch {
    // Malformed % escapes — fall back to literal-only.
  }
  for (const cand of candidates) {
    // Try relative to source's directory first
    const fromSource = resolve(dirname(sourcePath), cand);
    if (existsSync(fromSource)) return fromSource;
    // Also try from project root (some authors write `docs-canonical/X.md`)
    const fromRoot = resolve(projectDir, cand);
    if (existsSync(fromRoot)) return fromRoot;
  }
  return null;
}

/**
 * Collect canonical markdown docs to scan. Same selection logic as other
 * validators — `docs-canonical/`, root tracking files, and AGENTS.md.
 */
function collectCanonicalDocs(projectDir) {
  const docs = [];
  const cdir = resolve(projectDir, 'docs-canonical');
  if (existsSync(cdir)) {
    try {
      for (const f of readdirSync(cdir)) {
        if (f.endsWith('.md')) {
          const p = join(cdir, f);
          if (statSync(p).isFile()) docs.push(p);
        }
      }
    } catch {}
  }
  // Standard root-level docs that are commonly cross-referenced. We index
  // them so links like [CONTRIBUTING.md](CONTRIBUTING.md#some-section) can
  // resolve. The list is conservative — adding everything would pull in
  // boilerplate (LICENSE, NOTICE) that doesn't have meaningful headings.
  for (const f of [
    'README.md', 'AGENTS.md', 'CHANGELOG.md', 'DRIFT-LOG.md', 'ROADMAP.md',
    'CONTRIBUTING.md', 'CODE_OF_CONDUCT.md', 'SECURITY.md',
    'PHILOSOPHY.md', 'STANDARD.md', 'COMPARISONS.md',
  ]) {
    const p = resolve(projectDir, f);
    if (existsSync(p)) docs.push(p);
  }
  return docs;
}

/**
 * Validator entrypoint — matches the standard signature returning
 * { errors, warnings, passed, total }.
 *
 * v0.29: migrated to structured findings (XRF001–XRF002). Messages are
 * byte-identical to the legacy strings — resultFromFindings derives the
 * errors/warnings arrays from the same findings, so counts, exit codes, and
 * existing tests are unaffected; guard just renders richer output.
 */
export function validateCrossReferences(projectDir, config = {}) {
  const findings = [];
  const fixes = [];
  const wikiRefs = []; // validated in a second pass — evidence gate needs the full set
  let passed = 0;
  let total = 0;

  const docs = collectCanonicalDocs(projectDir);
  if (docs.length === 0) {
    return resultFromFindings([], { passed, total, applicable: false });
  }

  // Build a map of doc path → anchor set for fast lookups during ref resolution.
  const anchorIndex = new Map();
  for (const d of docs) {
    try {
      const content = readFileSync(d, 'utf-8');
      const headings = extractHeadings(content);
      anchorIndex.set(d, new Set(headings.map(h => h.anchor)));
    } catch {
      anchorIndex.set(d, new Set());
    }
  }

  // Walk every doc and validate each ref.
  for (const docPath of docs) {
    let content;
    try { content = readFileSync(docPath, 'utf-8'); } catch { continue; }
    const refs = extractRefs(content, docPath);
    const docName = basename(docPath);

    for (const ref of refs) {
      if (ref.wiki) {
        wikiRefs.push({ ...ref, docPath, docName });
        continue;
      }
      total++;

      // Resolve the target file (if any)
      let targetPath = null;
      if (ref.file) {
        // Skip non-markdown files — we only validate doc cross-refs, not
        // links to images / code / config files. Those have their own truth.
        if (!ref.file.toLowerCase().endsWith('.md')) {
          passed++;
          continue;
        }
        targetPath = resolveTarget(docPath, ref.file, projectDir);
        if (!targetPath) {
          findings.push(mkFinding({
            code: 'XRF001',
            validator: 'crossReference',
            severity: 'warn',
            message: `${docName}:${ref.line} — broken link: target file "${ref.file}" not found`,
            location: `${relative(projectDir, docPath)}:${ref.line}`,
            suggestion: { kind: 'fix', text: 'Fix the link target path (or remove the dead link)' },
          }));
          continue;
        }
      } else {
        // Intra-doc anchor reference
        targetPath = docPath;
      }

      // If there's an anchor, verify it resolves in the target doc.
      // URL-decode the anchor first — some editors percent-encode chars
      // like `️` (variation selector) in TOC links that wouldn't
      // appear in the actual GitHub-rendered anchor. Decoding makes
      // `#%EF%B8%8F-cicd-integration` compare against `#-cicd-integration`.
      if (ref.anchor) {
        let decodedAnchor;
        try {
          decodedAnchor = decodeURIComponent(ref.anchor);
        } catch {
          decodedAnchor = ref.anchor;
        }
        // After decode, re-apply the slug pipeline so we compare like-for-like.
        const normalizedAnchor = slugifyHeading(decodedAnchor);
        const anchors = anchorIndex.get(targetPath);
        const matches = anchors && (anchors.has(ref.anchor) || anchors.has(normalizedAnchor));
        if (!matches) {
          const where = targetPath === docPath ? 'same doc' : basename(targetPath);
          // S-12: suggest the closest matching anchor when there's a near-miss.
          const suggestion = anchors ? suggestAnchor(normalizedAnchor, anchors) : null;
          const hint = suggestion ? ` (did you mean #${suggestion}?)` : '';
          // v0.14.1-S12+: when the suggestion is HIGH-CONFIDENCE — unambiguous
          // and very close (edit distance <= 2) — emit a mechanical fix so
          // `docguard fix --write` resolves it without AI. Other near-misses
          // still get the hint but no fix (the user needs to verify intent).
          const isHighConfidence = suggestion && isUnambiguousSuggestion(normalizedAnchor, suggestion, anchors);
          findings.push(mkFinding({
            code: 'XRF002',
            validator: 'crossReference',
            severity: 'warn',
            message: `${docName}:${ref.line} — broken anchor: "#${ref.anchor}" in ${where} doesn't match any heading${hint}` +
              (isHighConfidence ? ' [auto-fixable]' : ''),
            location: `${relative(projectDir, docPath)}:${ref.line}`,
            suggestion: isHighConfidence
              ? { kind: 'fix', text: `Replace #${ref.anchor} with #${suggestion}`, command: 'docguard fix --write' }
              : { kind: 'review', text: 'Update the anchor to match a real heading in the target doc' },
          }));
          if (isHighConfidence) {
            fixes.push({
              type: 'replace-anchor',
              doc: relative(projectDir, docPath),
              line: ref.line,
              from: ref.anchor,
              to: suggestion,
              summary: `${docName}:${ref.line} #${ref.anchor} → #${suggestion}`,
            });
          }
          continue;
        }
      }

      passed++;
    }
  }

  // ── Wikilink pass — only when the repo demonstrably uses [[x]] as FILE
  // links: `.obsidian/` exists, or at least one wikilink target resolves.
  // Repos using [[name]] as a non-file convention are skipped silently.
  if (wikiRefs.length > 0) {
    const wikiIndex = buildWikiIndex(projectDir, config, docs);
    const resolved = wikiRefs.map(r => ({
      r,
      path: resolveWikiTarget(r.docPath, r.file, projectDir, wikiIndex),
    }));
    const evidence = existsSync(resolve(projectDir, '.obsidian')) || resolved.some(x => x.path);
    if (evidence) {
      for (const { r, path } of resolved) {
        total++;
        if (!path) {
          findings.push(mkFinding({
            code: 'XRF001',
            validator: 'crossReference',
            severity: 'warn',
            message: `${r.docName}:${r.line} — broken wikilink: target "[[${r.file}]]" not found`,
            location: `${relative(projectDir, r.docPath)}:${r.line}`,
            suggestion: { kind: 'fix', text: 'Fix the wikilink target (or remove the dead link)' },
          }));
          continue;
        }
        if (r.anchor) {
          let anchors = anchorIndex.get(path);
          if (!anchors) {
            try {
              anchors = new Set(extractHeadings(readFileSync(path, 'utf-8')).map(h => h.anchor));
            } catch { anchors = new Set(); }
            anchorIndex.set(path, anchors);
          }
          // Obsidian anchors are heading TEXT ([[Doc#Quick Start]]); compare
          // through the same slug pipeline as inline links.
          const normalized = slugifyHeading(r.anchor);
          if (!anchors.has(normalized) && !anchors.has(r.anchor)) {
            const suggestion = suggestAnchor(normalized, anchors);
            const hint = suggestion ? ` (did you mean #${suggestion}?)` : '';
            findings.push(mkFinding({
              code: 'XRF002',
              validator: 'crossReference',
              severity: 'warn',
              message: `${r.docName}:${r.line} — broken anchor: "[[${r.file}#${r.anchor}]]" doesn't match any heading in ${basename(path)}${hint}`,
              location: `${relative(projectDir, r.docPath)}:${r.line}`,
              suggestion: { kind: 'review', text: 'Update the wikilink heading to match a real heading in the target doc' },
            }));
            continue;
          }
        }
        passed++;
      }
    }
  }

  return { ...resultFromFindings(findings, { passed, total }), fixes };
}

/**
 * v0.14.1-S12+ — is the suggested anchor an unambiguous, high-confidence
 * match? Two criteria, both must hold:
 *   1. Edit distance between the broken anchor and the suggestion is <= 2
 *      (catches typos and minor renames, refuses major slug rewrites).
 *   2. The suggestion is the ONLY anchor that close — no other anchor
 *      within distance <= 2. Avoids fixing to the wrong candidate when
 *      multiple are similar.
 */
function isUnambiguousSuggestion(broken, suggestion, anchors) {
  if (!broken || !suggestion || !anchors) return false;
  const sugDist = editDistance(broken, suggestion);
  if (sugDist > 2) return false;
  // Count other anchors that are also within the same tight budget.
  let closeCandidates = 0;
  for (const a of anchors) {
    if (a === suggestion) continue;
    if (editDistance(broken, a) <= 2) closeCandidates++;
    if (closeCandidates > 0) return false; // ambiguous
  }
  return true;
}
